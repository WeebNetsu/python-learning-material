from math import * # imports some math functions from module

print(abs(-5)) # 5 (absolute value)
print(pow(5, 3)) # 5 ** 3 or 5^3 (125)
print(max(3, 5, 9, 1, 2)); # returns biggest number
print(min(8, 20, 10, 17, 15, 2, -7, 1)) # returns smallest number
print(round(3.14159)) # rounds to nearest whole number
print(round(3.1415, 2)) # rounds to a specific decimal

# EVERYTHING below this line needs the math import
print(floor(5.87)) # 5 rounds down
print(ceil(7.12)) # 8 round up
print(sqrt(25)) # 5 (5 * 5 = 25)