numers = list(range(10)) # list() converts to list(array)

print(numers) # lists values 0 - 9

nums = list(range(5))
print(nums[4]) # returns 4

between = list(range(10, 21)) # from 10 - 20
print(between)

steps = list(range(0, 101, 5)) # counts by 5 so: [0, 5, 10, 20...]
print(steps)

reverseSteps = list(range(20, -1, -2)) # counts backwards
print(reverseSteps)